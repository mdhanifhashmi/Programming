package javabean;

public class Hotel {
	
	private String name;
	private int ratings;
	
	//setter method
	public void setName(String name) {
		this.name = name;
	}
	
	public void setRatings(int ratings) {
		this.ratings = ratings;
	}
	
	//getter method
	public String getName() {
		return name;
	}
	
	public int getRatings() {
		return ratings;
	}

}
